// Get icon component by emoji or name
export const getIcon = (key, props = {}) => {
    const IconComponent = iconMap[key];
    if (IconComponent) {
        return <IconComponent {...props} />;
    }
    // Return null if no icon found (fallback)
    return null;
};

// Icon component with defaults
const Icon = ({
    name,
    size = 24,
    color = 'currentColor',
    strokeWidth = 2,
    className = '',
    ...props
}) => {
    const IconComponent = iconMap[name];

    if (!IconComponent) {
        console.warn(`Icon "${name}" not found in IconMap`);
        return null;
    }

    return (
        <IconComponent
            size={size}
            color={color}
            strokeWidth={strokeWidth}
            className={className}
            {...props}
        />
    );
};

